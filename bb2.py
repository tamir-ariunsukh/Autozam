# Function to create correlation matrix
def plot_correlation_matrix(df, title, columns):
    # Encoding categorical variables for correlation
    df_encoded = df[columns].copy()
    for col in df_encoded.columns:
        if df_encoded[col].dtype == "object":
            df_encoded[col] = pd.Categorical(df_encoded[col]).codes

    corr_matrix = df_encoded.corr()

    n = len(columns)
    fig, ax = plt.subplots(figsize=(max(12, n * 0.5), max(10, n * 0.5)))
    sns.heatmap(
        corr_matrix,
        annot=True,
        fmt=".2f",
        cmap="coolwarm",
        vmin=-1,
        vmax=1,
        center=0,
        ax=ax,
        annot_kws={"size": 8},  # Фонтын хэмжээ багасгаж эсвэл томруулж болно
    )
    ax.set_title(title, fontsize=18, fontweight="bold", pad=20)
    plt.xticks(fontsize=9, rotation=45, ha="right")
    plt.yticks(fontsize=9)
    plt.tight_layout()
    return fig


